
# Operators demo
# Arithmetic, relational, and logical operators

def main():
    x = 15
    y = 4
    print("x + y =", x + y)
    print("x - y =", x - y)
    print("x * y =", x * y)
    print("x / y =", x / y)
    print("x // y =", x // y)  # Floor division
    print("x % y =", x % y)
    print("x ** y =", x ** y)

    print("x > y:", x > y)
    print("x == y:", x == y)
    print("x != y:", x != y)


if __name__ == "__main__":
    main()