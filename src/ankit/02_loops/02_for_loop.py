
# For loop demo
# Iterating over a range and a list

def main():
    print("Using range:")
    for i in range(5):  # 0 to 4
        print(i)

    print("\nIterating over a list:")
    fruits = ["apple", "banana", "cherry"]
    for fruit in fruits:
        print(fruit)

if __name__ == "__main__":
    main()